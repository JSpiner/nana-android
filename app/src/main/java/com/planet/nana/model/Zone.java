package com.planet.nana.model;

public class Zone {

    private int id;
    private double latitude;
    private double longitute;
    private double radius;

    public int getId() {
        return id;
    }

    public double getLatitude() {
        return latitude;
    }

    public double getLongitute() {
        return longitute;
    }

    public double getRadius() {
        return radius;
    }
}
